    public void showData() {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clinicdata", "root", "admin123");
            Statement st = con.createStatement();
            String sql = "select *from clinicdata.bill";
            ResultSet rs = st.executeQuery(sql);

            DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
            tblModel.setRowCount(0);

            while (rs.next()) {
                String billNum = String.valueOf(rs.getInt("num"));
                String patientID = String.valueOf(rs.getInt("patient_id"));
                String username = rs.getString("patientName");
                String cost = rs.getString("cost");
                String servicesNum = rs.getString("servicesNum");
                String ClinicAssistant = String.valueOf(rs.getInt("clinicassistant_id"));

                //String Array for store data into jTable
                String tbData[] = {billNum, patientID, username, servicesNum, cost, ClinicAssistant};

                //Add String Array data into jTable
                tblModel.addRow(tbData);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


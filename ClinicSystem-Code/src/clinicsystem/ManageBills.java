    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        showData();
        String PatientName = PatientName_edt.getText();
        String Cost = Cost_edt.getText();
        String Services = ServicesNum_edt.getText();
        String PatientID = PatientID_edt.getText();
        String CA_ID = CAID_edt.getText();

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clinicdata", "root", "admin123");
            Statement st = con.createStatement();
            String sql = ("insert into bill(patientName,cost,servicesNum,patient_id,clinicassistant_id) values ('" + PatientName + "','" + Cost + "','" + Services + "','" + PatientID + "','" + CA_ID + "')");

            int rowAdded = st.executeUpdate(sql);

            if (rowAdded > 0) {
                JOptionPane.showMessageDialog(this, "Successfully New Bill has been Created! ");
                showData();
            } else {
                JOptionPane.showMessageDialog(this, "Please Fill All rows correct, Try again!", "Failure", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_AddBtnActionPerformed


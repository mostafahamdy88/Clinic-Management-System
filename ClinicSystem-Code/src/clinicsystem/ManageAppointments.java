    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:       
        String id = dePID_edt.getText();
        showData();
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clinicdata", "root", "admin123");
            Statement st = con.createStatement();
            String sql = "delete from clinicdata.appointment WHERE patient_id='" + id + "'";
            int a = JOptionPane.showConfirmDialog(null, "Are you sure that you want to delete this appointment ?", "Select", JOptionPane.YES_NO_OPTION);
            if (a == 0) {
                int rowsDeleted = st.executeUpdate(sql);
                if (rowsDeleted > 0) {
                    JOptionPane.showMessageDialog(this, "Appointment for patient ID: " + id + " has been deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "This patient ID not found", "Failure", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed


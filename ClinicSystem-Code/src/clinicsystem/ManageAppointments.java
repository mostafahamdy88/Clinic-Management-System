    private void checkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkBtnActionPerformed
        // TODO add your handling code here:
        String id = Pid_edt.getText();
        try {
            boolean flag = false;
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clinicdata", "root", "admin123");
            Statement st = con.createStatement();
            String sql = "select patient_id from clinicdata.appointment";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                String patientID = rs.getString("patient_id");
                if (id.equals(patientID)) {
                    flag = true;
                    break;
                }
            }
            if (flag) {
                JOptionPane.showMessageDialog(this, "This patient has already booked an appointment..");
            } else {
                JOptionPane.showMessageDialog(this, "This patient doesn't have an appointment", "Failure", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_checkBtnActionPerformed


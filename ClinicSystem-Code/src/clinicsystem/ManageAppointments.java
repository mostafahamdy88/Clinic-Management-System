    public void showData() {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clinicdata", "root", "admin123");
            Statement st = con.createStatement();
            String sql = "select *from clinicdata.appointment";
            ResultSet rs = st.executeQuery(sql);

            DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
            tblModel.setRowCount(0);

            while (rs.next()) {
                String dateTime = rs.getString("dateTime");
                String day = rs.getString("day");
                String patientID = String.valueOf(rs.getInt("patient_id"));
                String patientName = rs.getString("patientName");
                String ClinicAssistant = rs.getString("clinicassistant_id");
                //String Array for store data into jTable
                String tbData[] = {dateTime, patientName, patientID, day, ClinicAssistant};

                //Add String Array data into jTable
                tblModel.addRow(tbData);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


    private void endBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_endBtnActionPerformed
        // TODO add your handling code here:
        String PatientID = patientid_edt.getText();
        String DoctorID = doctorid_edt.getText();
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clinicdata", "root", "admin123");
            Statement st = con.createStatement();
            String sql = ("insert into treatment values ('" + PatientID + "','" + DoctorID + "')");
            int rowAdded = st.executeUpdate(sql);

            if (rowAdded > 0) {
                JOptionPane.showMessageDialog(this, "Successfully Patient Visit has been ended! ");
                showData();
                st.close();
                con.close();
            } else {
                JOptionPane.showMessageDialog(this, "Please Fill All rows correct, Try again!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_endBtnActionPerformed

